class Literals{
    public static void main(String [] args){
        int a=10;
        int b=017;//range in between 0-7
        int c=0xA7;//range in between 0-9, A-F (0-15)
        int d=0b0100;//range in between 0-1
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}